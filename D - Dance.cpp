#include<bits/stdc++.h>
#define int long long
using namespace std;
const int N = 100;
int g[N][N];
int n;

int st[N];

int ans = 0;

void dfs (int u, int sum)
{
	
	if (u > n) 
	{
		ans = max(ans, sum);
		
		return;
	}
	if (st[u] == 1)
	{
		dfs(u + 1, sum);
	}
	
	
	for (int i = u + 1; i <= n; i ++ )
	{
		if (!st[i] && !st[u])
		{
			st[u] = 1;
			st[i] = 1;
			dfs(u + 1, sum ^ g[u][i]);
			st[i] = 0;
			st[u] = 0;
		}
	}
}

signed main()
{
	cin >> n;
	n *= 2;
	
	for (int i = 1; i <= n; i ++ )
	{
		for (int j = i + 1; j <= n; j ++ )
		{
			cin >> g[i][j];
		}
	}
	dfs(1, 0);
	cout << ans << endl;
	return 0;
}


