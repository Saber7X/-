typedef struct node{
    int num_2, num_5;  //分别计数2和5的个数 
}node;
 
node Count(int num)   //计算数据num的质因子中2和5的数量，并存在结构体中（整数的质因子分解） 
{
    node temp = {0,0};
    while(num % 2 == 0)
    {
        temp.num_2++;
        num /= 2;
    }
    while(num % 5 == 0)
    {
        temp.num_5++;
        num /= 5;
    }
    return temp;
}  
 
